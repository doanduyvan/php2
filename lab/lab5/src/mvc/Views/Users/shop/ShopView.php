      function renderproducts(data){
        const ProductElement = document.getElementById("ProductElement");
        ProductElement.innerHTML = "";
        data.forEach(item => {
          const div = document.createElement("div");
        div.classList.add("col-lg-4","col-md-6");
        div.innerHTML = `
                <div class="single-product">
                    <div class="product-img">
                      <img
                        class="card-img dv_img"
                        src="public/img/imgproducts/${item.img_url}"
                        alt=""
                      />
                      <div class="p_icon">
                        <a href="#">
                          <i class="ti-eye"></i>
                        </a>
                        <a href="#">
                          <i class="ti-heart"></i>
                        </a>
                        <a href="#" class="addcart">
                          <i class="ti-shopping-cart"></i>
                        </a>
                      </div>
                    </div>
                    <div class="product-btm">
                      <a href="shop/showproduct/${item.id}" class="d-block">
                        <h4>${item.product_name}</h4>
                      </a>
                      <div class="mt-3">
                        <span class="mr-4">${item.price_sale} VND</span>
                        <del>${item.price} VND</del>
                      </div>
                    </div>
                  </div>
        `;
        ProductElement.appendChild(div);

          div.onclick = function(e){
            if(e.target.closest(".addcart")){
              e.preventDefault();
              console.log("addcart",item.id);
              $data = {
                idproduct : item.id,
                quantity : 1
              };
              const url = "cart/addcartpost";

              myFetch(url,$data,function(data){
                console.log(data);
                if(data.status == 1){
                  toast({
                    title: "success",
                    message: data.message,
                    type: "success"
                  });
                  quantityproduct.innerHTML = data.quantityproduct;
                }else{
                  toast({
                    title: "Warning",
                    message: data.message,
                    type: "warning"
                  });
                }

              });
              
            }
            
          }

        });
      }


      function myFetch(url,data,callback){
          fetch(url,{
          method: "POST",
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify(data)
        }).then(res => res.json())
        .then(data => {
          callback(data);
        })
        .catch(err => {
          load(0);
          toast({
            title: "Error",
            message: err.message,
            type: "fail"
          });
          console.log(err);
        });

      }


    </script>